/// Command to initiate an OAuth2 authorization_code flow.
#[derive(Debug, Clone)]
pub struct AuthorizeCmd {
    pub client_id: String,
    pub redirect_uri: String,
    pub scopes: Vec<String>,
    pub state: String,
    pub code_challenge: String,
    /// Only "S256" is accepted.
    pub code_challenge_method: String,
    /// Authenticated user issuing the authorization.
    pub user_id: i64,
    pub tenant_id: i64,
}

impl AuthorizeCmd {
    /// Validate the authorization command before entering the service.
    pub fn validate(&self) -> AppResult<()> {
        if self.client_id.trim().is_empty() {
            return Err(AppError::ValidationError(
                "client_id cannot be empty".to_string(),
            ));
        }
        if self.redirect_uri.trim().is_empty() {
            return Err(AppError::ValidationError(
                "redirect_uri cannot be empty".to_string(),
            ));
        }
        if self.scopes.is_empty() {
            return Err(AppError::ValidationError(
                "at least one scope is required".to_string(),
            ));
        }
        if self.code_challenge_method.as_str() != "S256" {
            return Err(AppError::ValidationError(
                "only S256 code_challenge_method is supported".to_string(),
            ));
        }
        if self.code_challenge.trim().is_empty() {
            return Err(AppError::ValidationError(
                "code_challenge cannot be empty".to_string(),
            ));
        }
        Ok(())
    }
}

/// Command to exchange an authorization code for a token pair.
#[derive(Debug, Clone)]
pub struct ExchangeCodeCmd {
    pub code: String,
    pub code_verifier: String,
    pub redirect_uri: String,
    pub client_id: String,
    pub client_secret: String,
}

impl ExchangeCodeCmd {
    /// Validate the exchange command before entering the service.
    pub fn validate(&self) -> AppResult<()> {
        if self.code.trim().is_empty() {
            return Err(AppError::ValidationError("code cannot be empty".to_string()));
        }
        if self.code_verifier.trim().is_empty() {
            return Err(AppError::ValidationError(
                "code_verifier cannot be empty".to_string(),
            ));
        }
        Ok(())
    }
}

