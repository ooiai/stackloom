use neocrates::response::error::{AppError, AppResult};

/// A registered OAuth2 client application (tenant-scoped).
#[derive(Debug, Clone)]
pub struct OAuthClient {
    pub id: i64,
    pub tenant_id: i64,
    pub name: String,
    pub client_id: String,
    /// The Argon2-hashed client secret. Never returned to callers.
    pub client_secret_hash: String,
    pub redirect_uris: Vec<String>,
    pub allowed_scopes: Vec<String>,
    pub status: i16,
    pub description: Option<String>,
}

/// An issued OAuth2 token pair.
#[derive(Debug, Clone)]
pub struct OAuthToken {
    pub id: i64,
    pub oauth_client_id: i64,
    pub user_id: i64,
    pub tenant_id: i64,
    pub access_token: String,
    pub refresh_token: String,
    pub scopes: Vec<String>,
    pub access_token_expires_at: i64,  // Unix timestamp seconds
    pub refresh_token_expires_at: i64, // Unix timestamp seconds
    pub revoked_at: Option<i64>,
}

/// Third-party OAuth provider binding.
