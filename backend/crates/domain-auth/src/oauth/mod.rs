/// User information returned by a third-party provider.
#[derive(Debug, Clone)]
pub struct OAuthProviderUserInfo {
    pub provider: String,
    pub provider_user_id: String,
    pub provider_username: Option<String>,
    pub provider_email: Option<String>,
    pub display_name: Option<String>,
}

/// Command to create a new OAuth2 client application.
///
/// The HTTP layer populates `tenant_id`, `name`, `client_secret`, `redirect_uris`,
/// `allowed_scopes`, and `description`. The infra service generates `client_id` (UUID)
/// and computes `client_secret_hash` (Argon2) before calling the repository.
#[derive(Debug, Clone)]
pub struct CreateOAuthClientCmd {
    pub tenant_id: i64,
    pub name: String,
    /// Plaintext secret — set by HTTP layer; will be hashed by service before persistence.
    pub client_secret: String,
    /// Public identifier — generated by service (UUID, no dashes).
    pub client_id: String,
    /// Argon2 hash of client_secret — set by service before calling repo.
    pub client_secret_hash: String,
    pub redirect_uris: Vec<String>,
    pub allowed_scopes: Vec<String>,
    pub description: Option<String>,
}

impl CreateOAuthClientCmd {
    /// Validate user-provided fields before entering the service.
    pub fn validate(&self) -> AppResult<()> {
        if self.name.trim().is_empty() {
            return Err(AppError::ValidationError("name cannot be empty".to_string()));
        }
        if self.client_secret.trim().is_empty() {
            return Err(AppError::ValidationError(
                "client_secret cannot be empty".to_string(),
            ));
        }
        if self.redirect_uris.is_empty() {
            return Err(AppError::ValidationError(
                "at least one redirect_uri is required".to_string(),
            ));
        }
        if self.allowed_scopes.is_empty() {
            return Err(AppError::ValidationError(
                "at least one scope is required".to_string(),
            ));
        }
        Ok(())
    }
}

